    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $Tag = Tag::find($id);
        $Tag->delete();

        session()->flash('success','The Tag is successfully deleted!');
        
        return redirect()->route('tags.index');
